    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        //Verificamos que todos los campos esten llenos
        if(validar()){
            //Verificamos si las contraseñas son iguales
            if(verificarPass()){
                
                id_Usu  = (int) tblUsuario.getValueAt(fila, 0);
                //llamamos el constructor para crear un objeto de tipo cliente
                Usuario miUsuario = new Usuario(nombre, paterno, materno, domicilio, celular, nickName, contraseña, id_Usu);

                try {
                    manager.getUsuarioDAO().modificar(miUsuario);
                    ImageIcon miIcono = new ImageIcon(getClass().getResource("/imgIconos/userActualizacion.png"));
                    JOptionPane.showMessageDialog(null, "<html><h2>Los cambios han sido guardados exitosamente</h2></html>",
                            "Actulización Exitosa", 0, miIcono);
                    //llamamos el método limpiarFormulario
                    limpiarFormulario();
                    //llamamos el método deshabilitar
                    deshabilitar();
                    //llamamos el método inicializarListaClientes
                    inicializarListaUsuarios();
                } catch (DAOException ex) {
                    mensajeError(ex);
                }
            }else{
                ImageIcon miIcono = new ImageIcon(getClass().getResource("/imgIconos/passDesigualJOP.png"));
                JOptionPane.showMessageDialog(null,"<html><h2>Las Contraseñas no son Iguales</h2></html>", "ERROR", 0,
                        miIcono);
            }//fin del else  
        }// fin del if validar

    }//GEN-LAST:event_btnActualizarActionPerformed

