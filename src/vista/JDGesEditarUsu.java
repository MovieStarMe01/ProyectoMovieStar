    /**
    * Método para llenar todos los campos con los datos obtenidos de nuestra tabla
    */
    private void llenarCampos() {
        //Obtenemos la fila seleccionada
        fila = tblUsuario.getSelectedRow();
        
        //Obtenemos los datos de la fila 
        nombre = (String) tblUsuario.getValueAt(fila, 1);
        paterno = (String) tblUsuario.getValueAt(fila, 2);
        materno = (String) tblUsuario.getValueAt(fila, 3);
        domicilio = (String) tblUsuario.getValueAt(fila, 4);
        celular = (String) tblUsuario.getValueAt(fila, 5);
        nickName = (String) tblUsuario.getValueAt(fila, 6);
        
        //Colocamos los datos obtenidos de la fila seleccionada en los campos
        txtNombre.setText(nombre);
        txtPaterno.setText(paterno);
        txtMaterno.setText(materno);
        txtDomicilio.setText(domicilio);
        txtCelular.setText(celular);
        txtNick.setText(nickName);
        
        //llamamos el método habilitar
        habilitar();
                
    }// fin del método llenar campos

