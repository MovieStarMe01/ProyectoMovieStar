    /**
     * Imprime un mensaje de error personalizado para aquellos errores que son
     * producidos por el acceso a la BD
     * @param  objeto de tipo DAOException
     */
    private void imprimirMensajeDeErrorDAO(DAOException ex) {
         //Si getMessage existe obtenemos su valor
        String mensajeError;

        try{
            mensajeError = "Mensaje: " + ex.getCause().getMessage();
        }catch(NullPointerException error){
            mensajeError = "ID Cliente no Encontrado";
        }// fin del catch

        JOptionPane.showMessageDialog(null, ex.getMessage()+"\n"+mensajeError,"ERROR",
                JOptionPane.ERROR_MESSAGE);
    }// fin del método imprimirMensajeDeErrorDAO

    /**
    * Método para colocar los titulos a nuestra tabla
    */
    private void inicializarListaClientes() throws DAOException {
        
        model = new ClientesTableModel(manager.getClienteDAO());
            
        //Asignamos el modelo y ponemos los titulos a ver en nuestra tabla
        tblClientes.setModel(model);
        model.updateModel();
        model.fireTableDataChanged();
    }// fin del método inicializarListaAutores

    /**
     * Método para llenar todos los campos con los datos obtenidos de nuestra tabla
     */
    private void llenarCampos() {
        //Obtenemos la fila seleccionada
        fila = tblClientes.getSelectedRow();
        
        //Obtenemos los datos de la fila 
        nombre = (String) tblClientes.getValueAt(fila, 1);
        paterno = (String) tblClientes.getValueAt(fila, 2);
        materno = (String) tblClientes.getValueAt(fila, 3);
        domicilio = (String) tblClientes.getValueAt(fila, 4);
        celular = (String) tblClientes.getValueAt(fila, 5);
        correo = (String) tblClientes.getValueAt(fila, 6);
        
        //Colocamos los datos obtenidos de la fila seleccionada en los campos
        txtNombre.setText(nombre);
        txtPaterno.setText(paterno);
        txtMaterno.setText(materno);
        txtDomicilio.setText(domicilio);
        txtCelular.setText(celular);
        txtCorreo.setText(correo);
        
        //llamamos el método habilitar
        habilitar();
                
    }// fin del método llenar campos

