    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        if(validar()){
            cli_ID = (int) tblClientes.getValueAt(fila, 0);
            //llamamos el constructor para crear un objeto de tipo cliente
            cliente miCliente = new cliente(nombre, paterno, materno, domicilio, celular, correo, cli_ID);
            
            try {
                manager.getClienteDAO().modificar(miCliente);
                JOptionPane.showMessageDialog(null, "Los cambios han sido guardados exitosamente");
                //llamamos el método limpiarFormulario 
                limpiarFormulario();
                //llamamos el método deshabilitar
                deshabilitar();
                //llamamos el método inicializarListaClientes
                inicializarListaClientes();
            } catch (DAOException ex) {
               mensajeError(ex);
            }// fin del catch
        }// fin del if validar
      
    }//GEN-LAST:event_btnActualizarActionPerformed

